<h1>Overview</h1>
<p>
	This page provides an overview of the altimetry variables available within the application and
	serves as a reference guide for their interpretation. The variables are organized into thematic
	categories displayed in the navigation panel on the left side of the page, including Time and
	Location, Sea Level Variables, Wind Speed and Wave Height Variables, and Variables for Data
	Editing. Select any variable from the sidebar to view its corresponding description, including its
	definition, physical meaning, and role in satellite altimetry data analysis. Key scientific terms
	appearing in the descriptions are provided as hyperlinks to external references for users who wish
	to explore the underlying concepts in greater detail. All variable descriptions are based on the
	RADS Data Manual (v4.7.1), ensuring that the information presented is consistent with the official
	documentation.
</p>
